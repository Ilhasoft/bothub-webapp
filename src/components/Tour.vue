<template>
  <v-tour
    :steps="steps"
    :options="myOptions"
    :name="name"
    class="caption"/>
</template>

<script>
export default {
  name: 'Tour',
  props: {
    name: {
      type: String,
      default: null,
    },
    stepCount: {
      type: Number,
      default: 0,
    },
  },
  data() {
    return {
      myOptions: {
        labels: {
          buttonSkip: this.$t('webapp.tutorial.skip'),
          buttonPrevious: this.$t('webapp.tutorial.previous'),
          buttonNext: this.$t('webapp.tutorial.next'),
          buttonStop: this.$t('webapp.tutorial.finish'),
        },
        highlight: true,
      },
      callbacks: {
        onPreviousStep: this.myCustomPreviousStepCallback,
        onNextStep: this.myCustomNextStepCallback,
      },
    };
  },
  computed: {
    steps() {
      if (!this.name) return [];
      let index = 0;
      const steps = [];
      for (index = 0; index < this.stepCount; index += 1) {
        steps.push({
          target: `#tour-${this.name}-step-${index}`,
          content: this.$t(`webapp.tutorial.${this.name}.step_${index}`),
        });
      }
      return steps;
    },
  },
  mounted() {
    // A dynamically added onStop callback
    this.callbacks.onStop = () => {
      document
        .querySelector('#v-step-0')
        .scrollIntoView({ behavior: 'smooth' });
    };
  },
  methods: {
    nextStep() {
      this.$tours['vue-tour'].nextStep();
    },
    showLastStep() {
      this.$tours['vue-tour'].currentStep = this.steps.length - 1;
    },
  },
};
</script>

